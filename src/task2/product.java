package task2;

//Question no::1.2

public class product {
	private int pid;
	private double price;
	private int quantity;
	
	

	public product(int pid, double price, int quantity) {
		super();
		this.pid = pid;
		this.price = price;
		this.quantity = quantity;
	}
	
	public int getPid() {
		return pid;
	}

	
	public double getPrice() {
		return price;
	}


	public int getQuantity() {
		return quantity;
	}
	
	
	
	

}
